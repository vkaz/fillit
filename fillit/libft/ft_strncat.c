#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		i;
	size_t	len;

	i = 0;
	len = ft_strlen(s1);
	while (s2[i] != '\0' && n > 0)
	{
		s1[len + i] = s2[i];
		i += 1;
		n -= 1;
	}
	s1[len + i] = '\0';
	return (s1);
}
